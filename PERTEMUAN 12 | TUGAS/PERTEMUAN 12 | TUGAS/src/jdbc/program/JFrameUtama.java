    private void list_idMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_list_idMouseClicked
        int selectedId = Integer.parseInt(list_id.getSelectedValue());
        Mahasiswa mhs = mahasiswaService.getById(selectedId);
        if (mhs != null) {
            txt_id_edit.setText(String.valueOf(mhs.getId()));
            txt_nama_edit.setText(mhs.getNama());
            txt_id_hapus.setText(String.valueOf(mhs.getId()));
            txt_nama_hapus.setText(mhs.getNama());
        }
    }//GEN-LAST:event_list_idMouseClicked

