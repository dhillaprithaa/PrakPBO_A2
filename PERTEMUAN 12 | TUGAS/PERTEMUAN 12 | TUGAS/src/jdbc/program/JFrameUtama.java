    private void loadAllMahasiswa() {
        List<Mahasiswa> mahasiswaList = mahasiswaService.getAll();
        DefaultListModel<String> listModelId = new DefaultListModel<>();
        DefaultListModel<String> listModelNama = new DefaultListModel<>();
        for (Mahasiswa mhs : mahasiswaList) {
            listModelId.addElement(String.valueOf(mhs.getId()));
            listModelNama.addElement(mhs.getNama());
        }
        list_id.setModel(listModelId);
        list_nama.setModel(listModelNama);
    }
