    private void btn_tambahkanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_tambahkanActionPerformed
        String nama = txt_nama.getText();
        Mahasiswa mhs = new Mahasiswa('1', nama);
        mahasiswaService.add(mhs);
        loadAllMahasiswa();
    }//GEN-LAST:event_btn_tambahkanActionPerformed

    private void btn_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editActionPerformed
        int id = Integer.parseInt(txt_id_edit.getText());
        String nama = txt_nama_edit.getText();
        Mahasiswa mhs = new Mahasiswa(id, nama);
        mahasiswaService.update(mhs);
        loadAllMahasiswa();
    }//GEN-LAST:event_btn_editActionPerformed

    private void btn_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapusActionPerformed
        int id = Integer.parseInt(txt_id_hapus.getText());
        mahasiswaService.delete(id);
        loadAllMahasiswa();
    }//GEN-LAST:event_btn_hapusActionPerformed

    private void btn_resetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_resetActionPerformed
        mahasiswaService.indexReset();
        loadAllMahasiswa();
    }//GEN-LAST:event_btn_resetActionPerformed

